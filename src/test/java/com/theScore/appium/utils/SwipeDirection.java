package com.theScore.appium.utils;

public class SwipeDirection {
    public int startX;
    public int startY;
    public int endX;
    public int endY;

    public SwipeDirection(int startX, int startY, int endX, int endY) {
        this.startX = startX;
        this.startY = startY;
        this.endX = endX;
        this.endY = endY;
    }
}
